package ch03;

public class CompareEx {
	public static void main(String[] args) {
		int a=65;
		char c='A';
		System.out.println(a==c); //비교연산자(Equal)
		// 결과값이 true로 나오는 이유는 아스키코드로 a가 65이기 때문
		System.out.println(a = c); //대입연산자
		System.out.println('A' != 65); //not equal
		// 같지 않냐(!=)<<는 질문에 대한 답이 아니다이기 때문에 False로 값이 나옴
		
		
		
	}

}
